#Stack - data structure that used last in first out principle
#Python list works as stack - append() adds an element to the end, pop() removes the last element
l1 = [10, 20, 30, 40, 50];
print(l1);
l1.append(60);
print(l1);
l1.pop();
print(l1);
l1.append(90);
print(l1);
l1.pop();
l1.pop();
print(l1);